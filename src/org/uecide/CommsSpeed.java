package org.uecide;

public class CommsSpeed {
    int speed;
    String name;
    
    public CommsSpeed(int s, String n) {
        speed = s;
        name = n;
    }

    public int getSpeed() {
        return speed;
    }
    
    public String getName() {
        return name;
    }

    public String toString() {
        return name;
    }
}

